for (const code of languages) {
    const filePath = path.join(localesDir, code, 'translation.json');
    if (fs.existsSync(filePath)) {
        try {
            const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));

            // Update landing features
            if (!content.landing) content.landing = {};
            if (featuresTranslations[code]) {
                for (const [key, value] of Object.entries(featuresTranslations[code])) {
                    content.landing[key] = value;
                }
            }

            fs.writeFileSync(filePath, JSON.stringify(content, null, 2));
            console.log(`Updated features in ${code}/translation.json`);
        } catch (e) {
            console.error(`Error updating ${code}:`, e);
        }
    } else {
        console.log(`File not found for ${code}, skipping.`);
    }
}
